#include<stdio.h>

int main() {
    float a, n = 0;
    scanf("%f", &a);
    if (a <= 150) {
        n = a * 0.4463; 
    }
    if (a > 150 && a <= 400) {
        n = 150 * 0.4463 + (a - 150) * 0.4663;
    }
    if (a > 400) {
        n = 150 * 0.4463 + 250 * 0.4663 + (a - 400) * 0.5663;
    }
    printf("%.1f\n", n);
    return 0;
}
